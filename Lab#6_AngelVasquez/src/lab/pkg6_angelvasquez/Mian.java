    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        try {
            if (codigoexist(tf_Codigo.getText()) == -1) {
                char nal = 'n';
                if (jr_nacional.isSelected()) {
                    nal = 's';
                }
                String col = "";
                if (jr_Amarillo.isSelected()) {
                    col += jr_Amarillo.getText() + ",";
                }
                if (jr_Azul.isSelected()) {
                    col += jr_Azul.getText() + ",";
                }
                if (jr_Blanco.isSelected()) {
                    col += jr_Blanco.getText() + ",";
                }
                if (jr_Rojo.isSelected()) {
                    col += jr_Rojo.getText() + ",";
                }
                if (jr_verde.isSelected()) {
                    col += jr_verde.getText() + ",";
                }
                prductos.add(new Producto(Integer.parseInt(tf_Codigo.getText()), tf_cnombre.getText(), (Double) sp_cantazu.getValue(), (Double) sp_cantal.getValue(), nal, (Integer) sp_clote.getValue(), col, (Double) sp_cprice.getValue(), (Integer) sp_cantidad.getValue(), jdc_fechavencimiento.getDate()));
                adimi.setListaproductos(prductos);
                adimi.escribirArchivo();
                inved.escribirArchivo("Se agregao una bebida");
                JOptionPane.showMessageDialog(jd_crearProducto, "Producto creado exitosamente");
                tf_Codigo.setText("");
                tf_cnombre.setText("");
                
                jr_nacional.setSelected(false);
                jr_Amarillo.setSelected(false);
                jr_Azul.setSelected(false);
                jr_Blanco.setSelected(false);
                jr_Rojo.setSelected(false);
                jr_verde.setSelected(false);
                jd_crearProducto.dispose();
            } else {
                JOptionPane.showMessageDialog(jd_crearProducto, "Cree otro codigo");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(jd_crearProducto, "Ocurrio un error"+e.getMessage());
        }
        Actualizar_Tabla();
    }//GEN-LAST:event_jButton1MouseClicked

